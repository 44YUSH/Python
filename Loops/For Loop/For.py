# Loops are used for Sequential traversal. For traversing list, string, tuples etc.
# LOOP1
nums=[1,2,3,4,5]

for val in nums:
    print(val)
else:
    print("-----")  # It's optional

# LOOP2
alphabets=["a","b","c","d","e"]

for val in alphabets:
    print(val)
else:
    print("-----")

# LOOP3
str="AAYUSH SITH"

for char in str:
    print(char)
else:
    print("-----")